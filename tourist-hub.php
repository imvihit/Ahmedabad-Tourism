            <p><img src="images/img22.jpg" class="img_l" alt="" /> Just west of Manek Chowk stands the magnificent Jama Masjid (Friday Mosque), built in 1423 during the reign of Ahmedabad’s founder Ahmed Shah I. While all around the mosque lies the hectic frenzy of the center of the old city, step through the gates (on the north, east and south sides) and the urban chaos falls away behind you, leaving you standing in a refuge of profound serenity, accompanied only by people in quiet prayer, and birds perching on the columns. <br />
              <br />
              The wide open courtyard, floored with white marble, is ringed by a columned arcade painted with giant Arabic calligraphy, and has a tank for ritual ablutions in the center. The mosque and arcades are built of beautiful yellow sandstone and carved with the intricate detail that mosques of this period are known for. While the two principal minarets flanking the main arched entranceway collapsed in the 1819 earthquake, their lower portions still stand. The main prayer hall has over 260 columns supporting the roof, with its 15 domes, making a walk through the hall a beautiful maze of light and shadows. In its Indo-Saracenic architecture, the mosque also contains many syncretic elements not necessarily obvious to the viewer: some of the central domes are carved like lotus flowers, closely related to the typical domes of Jain temples; and some of the pillars are carved with the form of a bell hanging on a chain, in reference to the bells that often hang in Hindu temples. On one of the innermost windows there is even a carving of an 'Om' symbol. <br />
              <br />
            Like at all religious sites, take proper care to be respectful of others’ beliefs; this is, after all, a current place of worship. Visitors are welcomed, but should maintain the quiet contemplative atmosphere, remove their shoes, and not disturb those in prayer. During prayer times, you should keep your head covered and women should not enter the main hall. </p>
