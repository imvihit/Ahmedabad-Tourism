  <p>Ahmedabad is a city with present population of 72&nbsp;lakhs&nbsp;(7.2&nbsp;million).<a href="http://en.wikipedia.org/wiki/Ahmedabad_BRTS#cite_note-population-0"></a>, which is likely to be 11 million by the year 2035. This would lead to agglomeration of surrounding settlements like&nbsp;Naroda&nbsp;and other smaller villages, which ultimately increases the area of the city, which may become 1,000&nbsp;km2&nbsp;in the year 2035. Moreover, about 1/3 of total as well as student population reside within walking distance from the proposed&nbsp;BRTS&nbsp;network. Thus, there is a growing need for greater accessibility to basic amenities and opportunities for mobility in the city. In such a state of rapid urbanization, it is very essential to have an efficient and rapid transit system, which will sustain and accelerate the growth of the city. In order to cater this future demand, the city and State Government has initiated a Plan for Integrated Public Transit System, in which Bus Rapid Transit System (BRTS) is one of the components. This will facilitate the major mobility need of the people. In future, this system will get integrated with&nbsp;Ahmedabad Metro&nbsp;by the addition of two lines running through east to west and north to south.</p>
  <p>In addition, after the implementation of Ahmedabad BRTS and&nbsp;MetroLink Express Gandhinagar and Ahmedabad,&nbsp;Gujarat International Finance Tec-City (GIFT)&nbsp;can be reached easily through multimodal mix of Rapid Transport Systems.</p>
  <p>Unlike other Indian BRTS projects, this project is on full-swing and the system designs are quite similar to&nbsp;Curitiba's&nbsp;Rede Integrada de Transporte&nbsp;andBogota's&nbsp;TransMilenio&nbsp;which are exceptionally better than&nbsp;Delhi BRTS&nbsp;and&nbsp;Pune BRTS. By following this system&nbsp;Chennai BRTS&nbsp;and&nbsp;Bangalore BRTS&nbsp;are also under implementation.</p>
  <p>Second half of the first phase of the BRTS was inaugurated on&nbsp;<strong>December 25, 2009</strong>, birthdate of&nbsp;Atal Bihari Vajpayee, former Prime Minister of India. Phase I was stretched up to&nbsp;Kankaria&nbsp;Lake, to cater eastern part of the city also. It was the first time that BRTS buses crossed river &amp; reached Maningar, the most developed area of the city. It was inaugurated by Gujarat Chief Minister Narendra Modi.</p>
  <p>Currently Ahmedabad BRTS carries more than 100,000 passengers daily.</p>
  <h2><span id="Milestones_Achieved_By_BRTS_This_Year_.282010.29">Milestones Achieved By BRTS This Year (2010)</span></h2>
  <p>Residents of Ahmedabad seem to be relying more on BRTS as an alternative to AMTS buses. During the festive season, BRTS recorded an all-time high income of Rs 5.88 lakh. Also, the number of passengers who used BRTS reached 95,614, a record so far.</p>
  <p>BRTS is turning out to be the most popular way to commute, especially from the western part of the city to the Kankaria lake. The bus stop at the lake is the most crowded station during the festive season. In the last four days beginning from November 6, about 16,829 passengers boarded the bus from Kankaria and 23,153 got down at the lake.</p>
  <p>Income from the Kankaria bus station alone, in the last four days, was more than Rs 2.52 lakh. A senior AMC official said that BRTS is proving to be more popular among people going for a outing to the lake. &quot;Even well-to-do families park their cars and board BRTS for Kankaria. This is the trend seen mainly on Sundays and holidays.&quot;</p>
  <p>He said that about 0.9 per cent people who travelled by car have switched over to BRTS. &quot;This is the segment of people who prefer BRTS to go to the Kankaria lake,&quot; said AMC officials.</p>
  <p>AMC officials said that there was a drop in the number of passengers during Diwali and the next day. Usually about 82,000 people travel in BRTS before it was extended to Naroda from Soni-ni-Chawl and the income was around Rs 4.5 lakh.</p>
  <p>The services were extended to Naroda on November 4. Officials said that the increase in number of passengers could also be because of this increase. AMC is expecting the number of travellers to cross the one lakh-mark once schools and factories reopen later this week.</p>
  <p>UC Padia, deputy municipal commissioner in charge of the BRTS project, said that around 57 per cent of those travelling by BRTS use the service to go to their work place and another 28 per cent use it to reach education institutes.</p>
  <p>This segment of travellers was replaced by local travellers who preferred BRTS over AMTS to travel during the festive period.</p>
  <p>Officials added that the AMTS income also registered an increase during the period. On Sunday, when there were 664 buses on the road, the income was about Rs 19.92 lakh, and on Monday, AMTS had 646 buses and the income was over Rs 22.5 lakh.</p>
  <p>Read more: Festive gains: BRTS holiday income hits all-time high - The Times of India&nbsp;http://timesofindia.indiatimes.com/city/ahmedabad/Festive-gains-BRTS-holiday-income-hits-all-time-high/articleshow/6904116.cms#ixzz15MI4ZUF4</p>
  <h2><span id="City_Transportation_Planning">City Transportation Planning</span></h2>
  <p>Prof.H.M Shivanand Swamy, an economist and urban planner by training and is the Associate Director,&nbsp;CEPT University&nbsp;spearheaded the planning and implementation of BRTS in&nbsp;Ahmedabad&nbsp;by mapping various corridors and feeder networks. Besides giving priority to buses, the system is designed to provide dedicated lanes for pedestrian and non-motorized vehicles like cycles and rickshaws etc.</p>
  <p><strong>Feasibility Report (2005)</strong>: First study initiated by the state project development agency,&nbsp;Gujarat Infrastructure Development Board, initiated preparation of a feasibility report for bus rapid transit system. The study, based on a detailed assessment of demand, socio-economic characteristics and technical feasibility, identified BRTS development over 58&nbsp;kilometers of road network.</p>
  <p><strong>Detailed Project Report (2005)</strong>: Based on the feasibility report prepared by&nbsp;CEPT, a detailed project report for submission under&nbsp;JNNURM&nbsp;program of the&nbsp;Government of India&nbsp;was prepared by&nbsp;CEPT. The project has been sanctioned by the&nbsp;<strong>Ministry of Urban Development</strong>,&nbsp;Government of India. The project report included detailed planning of roadway as well as development of other supporting infrastructure and operating systems.</p>
  <p><strong>Detailed System Design and Implementation Support (2006-ongoing)</strong>:BRTS- Ahmedabad is under implementation. Detailed system design and support during construction is being provided byCEPT&nbsp;team. The team had already made some 100 minor design changes to avoid the Delhi and Pune fiascos.</p>
  <h3><span id="Selection_of_Corridors">Selection of Corridors</span></h3>
  <p>Based on an analysis of the socio-economic factors, travel demand patterns, road network characteristics, proposed metro plan and existing&nbsp;Ahmedabad Municipal Transport Service&nbsp;(AMTS) route network as the criteria, a network of roads covering about 155&nbsp;kilometers in length have been identified for developing the Bus Rapid Transit System within Ahmedabad.</p>
  <p>Station - Interior. This station will cater to Gujarat Mineral Development Corporation Office, University Ground, AES (Ahmedabad Education Society)'s Ground and Physical Research Laboratory.</p>
  <h3><span id="Corridors">Corridors</span></h3>
  <p>Given the expected travel demand on each corridor, BRTS solutions are structured.The operations plan developed not only provides route structure for&nbsp;BRT&nbsp;operations but also the rationalised routing plan for&nbsp;AMTS&nbsp;operations. This is necessary so that the two systems compliment each other rather than compete with each other. In terms of routes following three types of routes are structured to operate transit services -&nbsp;BRT&nbsp;Trunk Routes, Complementary Routes (AMTS)and&nbsp;BRT&nbsp;Feeder Routes.</p>
