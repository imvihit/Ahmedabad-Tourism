            <img src="images/abadmetro11.jpg" alt="d" width="269" height="181" class="img_l" />
            <p> Set in the quiet village of Adalaj, this <span title="stepwell">vav</span> has served as a resting place for hundreds of years for many pilgrims and caravans along their trade routes. Built in 1499 by Queen Rudabai, wife of the Vaghela chief, Veersinh, this five-storey stepwell was not just a cultural and utilitarian space, but also a spiritual refuge. It is believed that villagers would come everyday in the morning to fill water, offer prayers to the deities carved into the walls and interact with each other in the cool shade of the vav. There is an opening in the ceilings above the landing which allows the light and air to enter the octagonal well. However, direct sunlight does not touch the flight of steps or landings except for a brief period at noon. Hence some researchers say that the atmosphere inside the well is six degrees cooler than the outside. Another remarkable feature of this stepwell is that out of the many stepwells in Gujarat, it is the only one with three entrance stairs. All three stairs meet at the first storey, underground in a huge square platform, which has an octagonal opening on top. The vav is a spectacular example of Indo-Islamic architecture and design. The harmonious play of intricate Islamic floral patterns seamlessly fusing into Hindu and Jain symbolism embody the culture and ethos of those times. All the walls carved by ornamentation, mythological scenes along with everyday scenes of women churning buttermilk, dancers accompanied by musicians, women adorning themselves and a king sitting on a stool. Fascinating to many visitors is the <em>Ami Khumbor</em> (a pot that contains the water of life) and the <em>Kalp Vriksha</em> (a tree of life) carved out of a single slab of stone. There is a belief that the small frieze of <em>navagraha</em> (nine-planets) towards the edge of the well protects the monument from evil spirits. </p>
