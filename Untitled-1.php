<h2><span id="Present_day">Present day</span></h2>
<p>The ashram now has a museum, Gandhi Smarak Sangrahalay. In 1963 the museum   building was erected (designed by architect Charles Correa), and memorial activities were   then started here.One of the important activities undertaken is the   establishment of a Gandhi Smarak Sangrahalaya. Initially started in 'Hriday   Kunj,' Gandhi's own cottage in the Ashram, the Sangrahalaya has now shifted to   its own well-designed and well-furnished building which was inaugurated by Jawaharlal Nehru, Prime Minister of India, on 10 May   1963.</p>
<p>Nandini: It is on the right hand side of 'Hridaya Kunj'. It is an old Ashram   guest house, where guests from India and abroad are put up.</p>
<p>Vinoba Kutir: Named after Acharya Vinoba Bhave who stayed here, and also   known as Mira Kutir after Miraben, Gandhiji's disciple, daughter of a British   Admiral.</p>
<p>Upasana Mandir:It is an open -air prayer ground, situated between 'Hridaya   Kunj' and 'Magan Kutir' (the hut where Maganlal Gandhi, the ashram manager, used   to stay).Here, Gandhiji used to refer to individual questions after prayers and   as the head of the family analysed and gave his solutions.</p>
