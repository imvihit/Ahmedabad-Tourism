                          <p>Off the eastern end of Nehru bridge stands the Sidi   Sayeed mosque. Built in 1573, it is the last of the major mosques to be built in   Ahmedabad under the Mughal rule. Surrounded by busy intersections, it presents a   stark contrast to speeding buses and giant advertisements. <br />
                                    <br />
                                  The carved <span title="">jaalis</span> in the windows of the western wall   are known worldwide and have become a symbol of the city of Ahmedabad. Depicting   a tree with intertwining branches, the carvings look like fine lace filigree   work, but are hewn from solid stone. Though much smaller than the Jama Masjid,   and lacking the enclosed courtyard, the craftsmanship of this mosque places it   on a level nearly unequaled in the world.<br />
