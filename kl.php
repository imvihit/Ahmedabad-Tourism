  <p><strong>Kankaria</strong> is the biggest lake in Ahmedabad (in earliar days it used to known, name by   Qutub Hojj). Where all kings, or badshah, used to do bath in this lake. of the   city of Ahmedabad, Gujarat, India. With an   approximate circumference of 1.4 miles (2.3&nbsp;km), it represents the regale   history of Ahmedabad. It is located in the southern part of the city in Maninagar area, which is the most densely populated   suburb of the city. Kankaria was built by Sultan Kutubuddin in 15th century. The   work was completed in 1451. Its ancient name is Hauj-E-Kutub.   It also consists of a water purification system but it is lost with the time. At   one point of the circular lake, there opens a walkway which later merges into a   garden called Nagina   Wadi (means beautiful garden in Urdu) that is   located in the center of the lake. There is also a gym in the Kankaria lake   campus which is called Ambubhai Vyayamshala.</p>
  <p>Lighting and special effects on the walkway and sumptuous food of the   restaurants in the central garden make the lake a worthwhile place to visit or   spend an evening. Kankaria is well-known for its junk   food in the entire province of Gujarat. At night, the junk food business   soars up. Especially in the summer between months of March and June.</p>
  <h2><span id="Renovation">Renovation</span></h2>
  <p>The inauguration ceremony for Rs 30 crore Kankaria lakefrontwas held on December   25, 2008.<a href="#cite_note-1"></a> The central garden   and the walkway has recently been revamped.</p>
  <p>Upon completion of the renovation the lake was officially inaugurated in a   week long event known as &quot;Kankaria Carnival&quot;. It is the first of its kind in the   history and is organised by Ahmedabad Municipal Corporation   (AMC).</p>
  <p>A train named 'Atal Express'(Named after former prime-minister of India Mr. Atal Bihari   Vajpayee on his birthday 25 December 2008) was imported from London, the train circles the lake on a 4.5&nbsp;km track at   a speed of 10&nbsp;km/h.[5] The train has the   capacity to carry 150 passengers, including 36 adults. The train is manufactured   by Severn Lamb.<a href="#cite_note-5"></a> During the first 11 months of introducing the train, it attracted nearly a   million visitors<a href="#cite_note-6"></a>. After the success   of this train, AMC is planning to order another of its kind, pending   approval<a href="#cite_note-7"></a>.</p>
  <p>The place is enjoyed as a place for yoga, walking, and running, especially in   the very early morning. There are also other attractions like a Zoo, Nagina   Wadi, Balvatika, an Aquarium and Gardens. Kankaria Zoo has also records in   breeding of rare species in Zoo which make them to feel that they are attached   with nature and not cages. Tourists/visitors will remember this place for years   together.</p>
