  <p>The&nbsp;<strong>Gujarat High Court</strong>&nbsp;(Gujarati:&nbsp;<span lang="gu" xml:lang="gu">&#2711;&#2753;&#2716;&#2736;&#2750;&#2724; &#2697;&#2714;&#2765;&#2714; &#2728;&#2765;&#2735;&#2750;&#2735;&#2750;&#2738;&#2735;</span>) is the&nbsp;High Court&nbsp;of the&nbsp;<a href="http://en.wikipedia.org/wiki/Indian_state" title="Indian state"></a>&nbsp;State of&nbsp;Gujarat. It was established on 1 May 1960 under the<em>Bombay Re-organsisation Act, 1960</em>&nbsp;after the state split from&nbsp;Bombay State.</p>
  <p>The seat of the court is&nbsp;Ahmedabad. The court has a sanctioned judge strength of 42.</p>
  <h2><span id="The_Chief_Justice">The Chief Justice</span></h2>
  <p>The current Chief Justice is Sudhanshu Jyoti Mukhopadhyaya, who assumed the charge on 9 December 2009.<a href="http://en.wikipedia.org/wiki/Gujarat_High_Court#cite_note-0"></a></p>
