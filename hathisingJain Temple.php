                          <td><p class="style1">Hutheesing Jain Temple</p>
                                  <p>This remarkably elegant temple created out of white marble has been sacred to many Jain families, generation after generation. It was built in 1848 A.D. at an estimated cost of 10&nbsp;<span title="100,000">lakh</span>&nbsp;rupees by a rich merchant Sheth Hutheesing as a dedication to the 15th Jain<span title="Jain Spiritual Figure">tirthankar</span>, Shri Dharmanatha. Traditional artisans working in stone belonged to the Sonpura &amp; Salat communities. The Salat community constructed masterpieces of architecture ranging from forts, palaces to temples. The work of the Hutheesing Jain temple is attributed to Premchand Salat. One scholar has remarked, &quot;Each part goes on increasing in dignity as we approach the sanctuary. Whether looked at from its courts or from the outside, it possesses variety without confusion and an appropriateness of every part to the purpose for which it was intended.&quot;&nbsp;<br />
                                      <br />
                                    Located outside the Delhi Gate, the temple is spread over a sprawling courtyard, a<span title="">mandapa</span>&nbsp;surmounted by a large ridged dome, which is supported by 12 ornate pillars. The small&nbsp;<em>garbhagruh</em>&nbsp;(main shrine) on the east end reaches up into three stunningly carved spires and encircled by 52 small shrines dedicated to the various Tirthankars. There are large protuding porches with magnificently decorated columns and figural brackets on three outer sides. Also a recently built 78 ft Mahavir&nbsp;<em>stambha</em>&nbsp;(tower) fashioned after the renowned tower at Chittor in Rajasthan, flanks the outer courtyard by the front entrance. Some of the motifs used in the design reminds one of the Sultanate minarets of the Mughal period.<br />
