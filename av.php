    <strong>Adalaj Stepwell</strong>(Gujarati:&#2693;&#2721;&#2750;&#2738;&#2716;&#2309;&#2337;&#2366;&#2354;&#2332;) is a unique Hindu 'water building' in the village of Adalaj, close to Ahmedabad town in Gandhinagar district in the Indian state of Gujarat. The stepwell was   built in 1499 by Muslim king Mohammed Begda for Queen Rani Roopba, wife of Veer   Singh, the Vaghela chieftain. The step well or   'Vav', as it is called in Gujarati, is intricately carved and is five   stories in depth. Such step wells were once integral to the semi arid regions of   Gujarat as they provided basic water needs for drinking, washing and bathing.   These wells were also venues for colorful festivals and sacred rituals.
    <p>Stepwells, also called stepped   ponds, built between the 5th and 19th centuries, are common in the west of India; over 120 such wells are reported in the semi-arid   region of Gujarat alone, of which the well at Adalaj is most popular. Stepwells are also found in more arid regions of the   subcontinent, extending into Pakistan, to collect   rain water during seasonal monsoons. While many such structures are utilitarian   in construction, they sometimes include significant architectural   embellishments, as in the Adlaj stepwell, which attracts a large number of   tourists. In the past, these stepwells were frequented by travelers and caravans   as stopovers along trade routes</p>
