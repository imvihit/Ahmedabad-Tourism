  <p>Current stands at Sardar Patel Stadium are as follow (in a clockwise direction from top)</p>
  <ul>
    <li>GMDC Upper and Lower Stands - Public seating</li>
    <li>East Upper and Lower Pavilion - Public seating</li>
    <li>Adani Lower Pavilion - Players dressing room, corporate boxes and public reserved seating</li>
    <li>Adani Upper Pavilion - Public seating</li>
    <li>Club Pavilion - Members seating</li>
    <li>West Pavilion - Public seating</li>
  </ul>
  <p>All stands are roof covered and have two tiers, barring the Club pavilion and West pavilion. The media is also well taken care of, with an air-conditioned press box. Press box is wi-fi enabled and also has a multi purpose media conference room. The stadium has car-park for 10,000 vehicles.<a href="http://en.wikipedia.org/wiki/Motera_Stadium#cite_note-crknxt-0">[1]</a>&nbsp;The Adani Pavilion also houses more than 20 air-conditioned corporate boxes. There are 21 entry gates to the stadium making it easier for spectators to enter the stands.</p>
