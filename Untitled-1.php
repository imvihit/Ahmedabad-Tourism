<h3><span id="Gandhi_Sangrhalaya">Gandhi Sangrhalaya</span></h3>
<ul>
  <li>&quot;My life is my message&quot; gallery, consisting of 8 life-size painting and more   than 250 photo-enlargements of some of the vivid and historic events of Gandhi's   life</li>
  <li>Gandhi in Ahmedabad Gallery, tracking Gandhi's life in Ahmedabad from   1915-1930</li>
  <li>Life-size oil painting gallery</li>
  <li>Gandhi quotations, letters and other relics exhibition</li>
  <li>Library consisting of nearly 35,000 books dealing with Gandhi's life, work,   teachings, Indian freedom movement and allied subjects, and a Reading Room with   more than 80 periodicals in English, Gujarati and Hindi</li>
  <li>Archives consisting of nearly 34,117 letters to and from Gandhi both in   original and in photocopies, about 8,781 pages of manuscripts of Gandhi's   articles appearing in <em>Harijan</em>, <em>Harijansevak</em>, and <em>Harijanbandhu</em> and about 6,000 photographs of Gandhi and his   associates</li>
  <li>An important landmark of the Ashram is 'Hridaya Kunj' where some of the   personal relics of Gandhi are on display</li>
  <li>Ashram book store, a non-profit book store selling literature and   memorabilia related to Gandhi and his life work and also supports local   artisans.
