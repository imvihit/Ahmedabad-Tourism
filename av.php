    <p>Built in sand stone in Indo-Islamic architectural style, the Adlaj stepwell   is five stories deep. It is octagonal (8-sided polygon) in plan at the top,   built on intricately carved large number of pillars. Each floor is spacious   enough to provide for people to congregate. It was dug   deep to access ground water at that level, accounting for seasonal fluctuations   in water level due to rainfall over the year. The air and light vents in the   roofs at various floors and at the landing level are in the form of large   openings. From the first story level, three staircases lead to the bottom water   level of the well, which is considered a unique feature. Built along a   North-South axis, entrance is from the South, the three staircases are from the   South, West and East directions leading to the landing, which is on the northern   side of the well. Four small rooms with oriel windows decorated with minutely carved   brackets are provided at the landing level, at the four corners. The structural   system is typically Indian style with traditional trabeat with horizontal beams and   lintels. At the bottom of the well is a square stepped floor in the shape of a   funnel extending to the lowest plane. This is chiseled into a circular well.   Above the square floor, columns, beams, wall and arched openings spiral around;   a feature that continues to the top. The top part of the well, however, is a   vertical space open to the sky. The four corners of the square are strengthened   with stone beams, set at 45 degrees angle. The motifs of   flowers and graphics of Islamic architecture blend very well with the symbols of   Hindu and Jain gods carved   at various levels of the well. The dominant carvings on the upper floors are of   elephants (3&nbsp;inches (76&nbsp;mm) in size, each of different design). The Islamic   architectural style could be attributed to the Muslim king Begda who built it.   The walls are carved with women performing daily chores such as churning of   buttermilk, adorning themselves, scenes of performance of dancers and musicians,   and the King overlooking all these activities.</p>
