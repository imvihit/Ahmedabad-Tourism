<h3><span id="Ashram_Activities">Ashram Activities</span></h3>
<ul>
  <li>The Sabarmati ashram which receives about 7 lakh (700,000) visitors a year houses, photocopies of about 34,000 letters written by   Gandhi and about 8000 photographs and 165 films and of course, Hriday Kunj, the   house where Gandhi and Kasturba stayed from 1918 to 1930.</li>
  <li>Collecting, processing, preserving and displaying archival materials   (writings, photographs, paintings, voice-records, films, personal clothes   etc</li>
  <li>A charkha' used by Gandhi to weave khadi and the writing table he used for   writing letters are also few of the priceless items kept here.</li>
  <li>Microfilming, lamination and preservation of negatives</li>
  <li>Arranging exhibitions on aspects of Gandhi&rsquo;s life, literature and   activities</li>
  <li>Publication of the &quot;Mahadevbhani Dairy,&quot; which chronicles the entire history   of the Indian freedom struggle</li>
  <li>The Ashram Trust funds activities that include education for the visitor,   the community and routine maintenance of the museum and its surrounding   grounds</li>
  <li>Keeping contact with as well as helping and undertaking study and research   in Gandhian thought and activities and publishing the results of such study and   research and allied literature for the benefit of the people</li>
  <li>Observance in a suitable manner of occasions connected with Gandhi&rsquo;s   life</li>
  <li>Maintaining contact with the youth and student community and providing   facilities to them for the study of Gandhian thought</li>
</ul>
<h3><span id="A_Walk_in_Gandhi_Ashram">A Walk in Gandhi   Ashram</span></h3>
<p>With prior appointment from The Secreretary, Gandhi Ashram Preservation &amp;   Memorial Trust, Gandhi Smarak Sangralaya, Gandhi Ashram Ahmedabad 380 027   walking tour can be organised. This 90 minutes guided tour starts with slide   show and ends at Library. This guided tour visits places as follows: 1) Magan   Niwas 2) Upasana Mandir - Prayer Ground 3) Hriday Kunj - House of Mahatma. 4)   Vinoba-Mira Kutir One hut where Vinoba Bhave &amp; Ms. Madeleine, British   admiral's daughter stayed. 5) Nandini</p>
