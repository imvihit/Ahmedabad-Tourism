  <p><strong>Motera has witnessed some great feats of Indian cricket history</strong>:</p>
  <ul>
    <li><a href="http://en.wikipedia.org/wiki/Sunil_Gavaskar">Sunil Gavaskar</a>&nbsp;completed 10,000 runs in Test cricket in 1986-87 against&nbsp;<a href="http://en.wikipedia.org/wiki/Pakistani_cricket_team" title="Pakistani cricket team">Pakistan</a>.</li>
