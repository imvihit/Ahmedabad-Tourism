<h2><span id="History">History</span></h2>
<p>The ashram was originally established at the Kocharab Bungalow of Jivanlal   Desai on 25 May 1915. The Ashram was then shifted on 17 June 1917 to a piece of   open land on the banks of the river Sabarmati. Reasons for this shift included:   Gandhi wanted to do some experiments in living (e.g. farming, animal husbandry, cow breeding, khadi and related   constructive activities for which he was in search of this kind of barren land.)   It was believed that this was ancient ashram site of Dadhichi   Rishi who had donated his bones for a righteous war, but his actual ashram   lies in Naimisharanya, near Lucknow, Uttar Pradesh; it is between a jail and a   crematorium, and he believed that a satyagrahi has invariably to go to either place. Mahatma   Gandhi said, &quot;This is the right place for our activities to carry on the   search for truth and develop fearlessness, for on one   side are the iron bolts of the foreigners, and on the other the thunderbolts of   Mother Nature.&quot;</p>
<p>While at the Ashram, Gandhi formed a tertiary school that mainly focused on   manual labour, agriculture, and literacy to advance his efforts for nation's   self-sufficiency. It was also from here on the 12 March 1930 that Gandhi marched   towards Dandi, 241 miles from the Ashram with 78 companions in protest of the British Salt Law,   which taxed Indian salt in an effort to promote sales of British salt in India.   This mass awakening filled the British jails with 60,000 freedom fighters. Later   the government seized their property, Gandhi, in sympathy with them, responded   by asking the Government to forfeit the Ashram. Then Government, however, did   not oblige. He had by now already decided on 22 July 1933 to disband the Ashram,   which later became asserted place after the detention of many freedom fighters,   and then some local citizens decided to preserve it. On 12 March 1930 he vowed   that he would not return to the Ashram until India won independence. Although   this was won on 15 August 1947, when India was declared a free nation, Gandhi   was assassinated in January 1948.</p>
