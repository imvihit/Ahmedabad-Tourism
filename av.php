    <p>An interesting depiction carved from a single block of stone is of the <em>Ami   Khumbor</em> (symbolic pot of the water of life) and the <em>Kalp Vriksha</em> (a   tree of life). Also seen is a fresco of navagraha or nine planets. These depictions are said to attract villagers for worship   during marriage and other ritualistic ceremonies.</p>
    <p>It has been implied that the temperature inside the well is about five   degrees lower than the outside hot summer temperatures. It is one of the reasons   for the ladies who came to fetch water from the well spending considerable time   in the cool climes here. They not only collect water but also worship the gods   and goddesses depicted at various levels and while away time in good   chatter.</p>
    <p>A tribute paid to the rich underground structures, which are intricately   decorated with sculptures, is that they are said to resemble palaces.<a href="#cite_note-define-5"></a></p>
    <h2><span id="Visitor_information">Visitor information</span></h2>
    <p>The Adalaj step-well is a popular tourist attraction of the Ahmedabad city and is situated 18&nbsp;kilometres (11&nbsp;mi)   north of the city.<a href="#cite_note-ahmed-4"></a> It is   5&nbsp;kilometres (3.1&nbsp;mi) from Gandhinagar, the   capital city of Gujarat.</p>
    <p>Ahmedabad is well connected by road, rail and air links with the rest of the   country. The international airport at Ahmedabad, known as the Sardar Vallabhbhai   Patel Airport, has flights operating to several countries. Kalupur is the railway station closest to the   stepwell.<a href="#cite_note-adalaj-1"></a></p>
