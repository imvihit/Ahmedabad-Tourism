  <p><strong>Sardar Patel Stadium</strong>&nbsp;(Motera Stadium) is one of the premier&nbsp;<a href="http://en.wikipedia.org/wiki/Cricket">Cricket</a>&nbsp;<a href="http://en.wikipedia.org/wiki/List_of_Test_cricket_grounds" title="List of Test cricket grounds">stadiums</a>&nbsp;of&nbsp;<a href="http://en.wikipedia.org/wiki/India">India</a>&nbsp;located in the&nbsp;<a href="http://en.wikipedia.org/wiki/Motera">Motera</a>&nbsp;locality of&nbsp;<a href="http://en.wikipedia.org/wiki/Ahmedabad">Ahmedabad</a>. Because of its location, the stadium is commonly called&nbsp;<em>Motera Stadium</em>&nbsp;to avoid confusion with another&nbsp;<a href="http://en.wikipedia.org/wiki/Sardar_Vallabhbhai_Patel_Stadium,_Ahmedabad" title="Sardar Vallabhbhai Patel Stadium, Ahmedabad">stadium of the same name</a>&nbsp;in Navrangpura locality of Ahmedabad. Sardar Patel stadium is owned by&nbsp;<a href="http://en.wikipedia.org/wiki/Gujarat_Cricket_Association">Gujarat Cricket Association</a>&nbsp;and comes under the aegis of the West Zone. It is the largest stadium in the state of&nbsp;<a href="http://en.wikipedia.org/wiki/Gujarat">Gujarat</a>, and has an official capacity of 54,000 spectators.<a href="http://en.wikipedia.org/wiki/Motera_Stadium#cite_note-crknxt-0">[1]</a>&nbsp;It is equipped with floodlights for day-and-night games and is a regular venue for&nbsp;<a href="http://en.wikipedia.org/wiki/Test_cricket">Test cricket</a>&nbsp;and&nbsp;<a href="http://en.wikipedia.org/wiki/One_Day_Internationals" title="One Day Internationals">One Day Internationals</a>&nbsp;(ODI) matches.</p>
  <p>Formerly known as the Gujarat Stadium, the ground was later renamed in tribute to&nbsp;<a href="http://en.wikipedia.org/wiki/Sardar_Vallabhbhai_Patel" title="Sardar Vallabhbhai Patel">Sardar Vallabhbhai Patel</a>, India's first&nbsp;<a href="http://en.wikipedia.org/wiki/Home_Minister" title="Home Minister">Home Minister</a>&nbsp;and&nbsp;<a href="http://en.wikipedia.org/wiki/Deputy_Prime_Minister_of_India" title="Deputy Prime Minister of India">Deputy Prime Minister</a>. Apart from cricket, the ground has played host to a number of programs arranged by the&nbsp;<a href="http://en.wikipedia.org/wiki/Government_of_Gujarat">Government of Gujarat</a>. The pitch once favoured the bowlers, but has lately been host to competitive games.</p>
  <p>As of March 2011, the Motera stadium has played host to 22 ODI matches. In India, only&nbsp;<a href="http://en.wikipedia.org/wiki/Eden_Gardens">Eden Gardens</a>&nbsp;(25 ODI matches) and&nbsp;<a href="http://en.wikipedia.org/wiki/M._Chinnaswamy_Stadium">M. Chinnaswamy Stadium</a>&nbsp;(23 ODI matches) have hosted more ODI matches than Motera Stadium.<a href="http://en.wikipedia.org/wiki/Motera_Stadium#cite_note-stadiumrecord-2">[3]</a>&nbsp;The stadium was also one of the host venues for&nbsp;<a href="http://en.wikipedia.org/wiki/2011_Cricket_World_Cup">2011 Cricket World Cup</a>&nbsp;held in India. It hosted 3 matches, including a Quarter final match between India and Australia.</p>
  <h2>[<a href="http://en.wikipedia.org/w/index.php?title=Sardar_Patel_Stadium&amp;action=edit&amp;section=1" title="Edit section: History and Development">edit</a>]<span id="History_and_Development">History and Development</span></h2>
  <p>Before Sardar Patel Stadium (Motera Stadium) came into existence, International cricket matches in the city were played at&nbsp;<a href="http://en.wikipedia.org/wiki/Ahmedabad_Municipal_Corporation" title="Ahmedabad Municipal Corporation">Ahmedabad Municipal Corporation's</a>&nbsp;<a href="http://en.wikipedia.org/wiki/Sardar_Vallabhbhai_Patel_Stadium,_Ahmedabad" title="Sardar Vallabhbhai Patel Stadium, Ahmedabad">stadium of the same name</a>&nbsp;(Sardar Patel Stadium) in the Navrangpura locality of the city. In 1982, the Government of Gujarat donated a 50-acre (200,000 m2) stretch of land on the banks of the&nbsp;<a href="http://en.wikipedia.org/wiki/Sabarmati_River">Sabarmati River</a>&nbsp;for the construction of new stadium. The construction of the Sardar Patel Stadium was carried out in a span of nine months and since then all International cricket fixtures for the city are hosted here. The stadium hosted its first International&nbsp;<a href="http://en.wikipedia.org/wiki/Test_cricket" title="Test cricket">Test match</a>&nbsp;on November 12, 1983 between&nbsp;<a href="http://en.wikipedia.org/wiki/Indian_cricket_team" title="Indian cricket team">India</a>&nbsp;and the&nbsp;<a href="http://en.wikipedia.org/wiki/West_Indies_cricket_team" title="West Indies cricket team">West Indies</a>. Later in the 1984-85 Australia-India series, Sardar Patel Stadium went on to host its first&nbsp;<a href="http://en.wikipedia.org/wiki/One_Day_International" title="One Day International">ODI</a>, one in which&nbsp;<a href="http://en.wikipedia.org/wiki/Australia_cricket_team" title="Australia cricket team">Australia</a>&nbsp;beat the&nbsp;<a href="http://en.wikipedia.org/wiki/Indian_cricket_team" title="Indian cricket team">Indians</a>.</p>
  <p><a href="http://en.wikipedia.org/wiki/Sunil_Gavaskar">Sunil Gavaskar</a>&nbsp;became the first-ever cricketer to score 10,000 runs in Test cricket on this ground &ndash; a feat he achieved against Pakistan. Seven years later&nbsp;<a href="http://en.wikipedia.org/wiki/Kapil_Dev">Kapil Dev</a>&nbsp;claimed his 432nd Test wicket on this ground to become the highest wicket taker in the world, breaking&nbsp;<a href="http://en.wikipedia.org/wiki/Richard_Hadlee" title="Richard Hadlee">Sir Richard Hadlee's</a>record for the most number of wickets in Test matches here. Before he became the highest wicket taker, Kapil Dev also made a nine-wicket haul in his career here at the Motera ground. In 1996, the ground hosted a low-scoring Test match against&nbsp;<a href="http://en.wikipedia.org/wiki/South_Africa_national_cricket_team" title="South Africa national cricket team">South Africa</a>, where the tourists faltered while chasing a paltry 170 for a win to end up at 105 all out.&nbsp;<a href="http://en.wikipedia.org/wiki/Javagal_Srinath">Javagal Srinath</a>&nbsp;scalped six wickets in the fourth innings of the match. However, South Africa had their sweet revenge when they bowled India out for 76 runs in the first session of the Test match in 2008 and then went on to win it by an innings and 90 runs. Sachin Tendulkar became the first-ever cricketer to score 18,000 runs in One day cricket in a match against&nbsp;<a href="http://en.wikipedia.org/wiki/Australia_cricket_team" title="Australia cricket team">Australia</a>&nbsp;during 2011 Cricket World Cup.</p>
  <p>The Stadium was also one of the venues for the 2006 edition of the&nbsp;<a href="http://en.wikipedia.org/wiki/ICC_Champions_Trophy">ICC Champions Trophy</a>, with five of the 15 games being played here. It has also hosted one game in each of the two&nbsp;<a href="http://en.wikipedia.org/wiki/Cricket_World_Cup" title="Cricket World Cup">Cricket World Cups</a>&nbsp;that were played in the country, including the first match of the 1996 World Cup between&nbsp;<a href="http://en.wikipedia.org/wiki/England_cricket_team" title="England cricket team">England</a>&nbsp;and&nbsp;<a href="http://en.wikipedia.org/wiki/New_Zealand_cricket_team" title="New Zealand cricket team">New Zealand</a>.</p>
  <p>The Stadium is an adopted home venue for the Indian Premier League franchise&nbsp;<a href="http://en.wikipedia.org/wiki/Rajasthan_Royals">Rajasthan Royals</a>. The Stadium hosted four of the seven home games of Rajasthan Royals during the 2010 season of the&nbsp;<a href="http://en.wikipedia.org/wiki/Indian_Premier_League">Indian Premier League</a>. Rajasthan Royals won three out of those four games played here.</p>
