  <h2><span id="Ahmedabad_Janmarg_Limited">Ahmedabad Janmarg Limited</span></h2>
  <p>Ahmedabad Janmarg Limited (AJL), the parent company which would govern BRTS operations in the city, is constituted as a Special Purpose Vehicle by&nbsp;Ahmedabad Municipal Corporation, Ahmedabad Urban Development Authority and&nbsp;Government of Gujarat. It is presently envisaged that private operators selected through competitive bidding will carry out bus operations.</p>
  <p>AJL will soon introduce smart cards for commuters. Regular BRTS passengers will be able to buy these smart cards, which will function similar to the monthly pass system for state transport and AMTS buses. The AJL will, however, offer many schemes for the users of their smart cards. The commuters will be able to buy the smart cards at&nbsp;INR&nbsp;500 apiece and the fare for each journey will be deducted from this amount. For example, for a journey from the RTO to Nehrunagar costing&nbsp;INR&nbsp;5, this amount will be deducted from the&nbsp;INR&nbsp;500 paid for the smart card. Hence, the commuters will not have to carry money for the fare every time they travel on the BRTS buses. At present, paper tickets are available at the special ticket windows at the BRTS stations. There are turnstile machines near the ticket windows, where the commuters can have their tickets punched to allow them entry inside the stations.</p>
  <h2><span id="Buses">Buses</span></h2>
  <p>The buses currently operating on the BRTS are Tata Buses, which don't have&nbsp;air conditioning&nbsp;and are frequently breaking down.<a href="http://en.wikipedia.org/wiki/Ahmedabad_BRTS#cite_note-1"></a>The AMC is now looking into getting&nbsp;Volvo&nbsp;or&nbsp;Mercedes Benz&nbsp;buses that have air-conditioning and have been used successfully in other BRTS around the world.<a href="http://en.wikipedia.org/wiki/Ahmedabad_BRTS#cite_note-2"></a></p>
