<h2><span id="History">History</span></h2>
<p>It is believed that the mosque was constructed by Sidi Bashir, a slave of Sultan&nbsp;Ahmed Shah. A conflicting story is that the mosque was built by Malik Sarang, a noble in the court of Muhammed Begada, another Sultan of&nbsp;Gujarat. The mosque was completed in 1452.</p>
<h2><span id="Minarets">Minarets</span></h2>
<p>There are two&nbsp;minarets&nbsp;in the mosque, each of which is three stories tall with carved balconies. A gentle shaking of either minaret results in the other minaret vibrating after a few seconds, though the connecting passage between them remains free of vibration[<em>citation needed</em>]. The actual cause of this is unknown as of yet. This phenomenon was first observed in the 19th century by Monier M. Williams, an English&nbsp;Sanskrit&nbsp;scholar.&nbsp;<a href="http://en.wikipedia.org/wiki/Jhulta_Minar#cite_note-1"></a></p>
<h2><span id="Other_shaking_minarets">Other shaking minarets</span></h2>
<p>Another mosque in Ahmedabad called the Raj Bibi mosque also had shaking minarets similar to the ones at the Sidi Bashir mosque. But theBritish&nbsp;dismantled one of the minarets in order to study the construction. The minaret could not be put back together[<em>citation needed</em>].</p>
<p>There is also one in Isfahan, Iran called&nbsp;Monar Jonban&nbsp;(shaking minarets) with almost the same properties.</p>
<h2><span id="Present_condition">Present condition</span></h2>
<p>The mosque is located opposite the Ahmedabad Railway station and is one of the most popular tourist destinations of Ahmedabad. Entry to the shaking minaret was prohibited following an incident atQutb Minar&nbsp;in&nbsp;Delhi, where a stampede resulted in many children being crushed.</p>
