    <h2><span id="Etymology">Etymology</span></h2>
    <p>While in Gujarati and Marwari language, the stepwell is called a <em>vav</em>, (leading down to the level of water), in other Hindi-speaking   regions of North India it is known as a <em>baoli</em> (also spelt,   &lsquo;bawdi&rsquo;, &lsquo;bawri&rsquo; and &lsquo;bavadi&rsquo;).<a href="#cite_note-define-5"></a></p>
    <h2><span id="History">History</span></h2>
    <p>A research scholar, who studied the history and architecture of the stepwells   in Gujarat under a Fulbright Fellowship, has termed these   wells as &ldquo;High Hindu Stepwells&rdquo; because of the recorded literature of the Brahmins of the period from fifth to   ninth centuries, during the &ldquo;High Hindu period&rdquo;. While the Brahmins were the architects, the   builders were artisans of Sompara sect of low&ndash;caste Hindus. A wide unbridgable gulf of   religious distinction existed between the two groups, with the former getting   all the credit</p>
    <p>Before the history of Adalaj Stepwell is stated, it would be informative to   mention that the first rock-cut step wells in India are dated from 200 AD to 400   AD. Subsequently, the wells at Dhank (550-625) and construction of stepped ponds   at Bhinmal (850-950) took place.<a href="#cite_note-7"></a></p>
    <p>The city of Mohenjo-daro has wells, which   may be the predecessor of the step well; as many as 700 wells have been   discovered in just one section of the city leading scholars to believe that   'cylindrical brick lined wells' were invented by the people of the Indus Valley Civilization.Between third and   second millineum BC, at the 'Great Bath', at the site of Mohenjodaro of the Harappan civilization, filling of water   was achieved from a large well located in one of the rooms in front of the open   courtyard of the building&ndash;complex.<a href="#cite_note-hooja-9"></a></p>
    <p>While early stepwells were made of stone, later step wells were made of   mortar, stucco, rubble and laminar stones. The well cylinder was the basic form   used to deepen the wells. It is also inferred that the Stepwells in Gujarat have   survived so long in view of the builder&rsquo;s knowledge of the soil conditions and   the earthquake proneness of the region.<a href="#cite_note-10"></a></p>
    <p>The well size recommended, based on considerations of stability, was of four   to thirteen <em>hasta</em> (&lsquo;hasta&rsquo; a Sanskrit word, which means &ldquo;forearm&rdquo; of size   varying from 12&ndash;24 inches (300&ndash;610 mm)), A size of eight <em>hasta</em> was   considered ideal and a 13 <em>hasta</em> well was considered dangerous. However,   the well thickness from top to bottom remained generally uniform.<a href="#cite_note-11"></a> By the 11th   century, the step well planning and design acquired architectural excellence and   the Hindu Stepwells were standardized.<a href="#cite_note-12"></a></p>
    <p>The history of the Adalaj step-well built in 1498 is established by an   inscription in Sanskrit found on a marble slab positioned in a recess on the   first floor, from the eastern entry to the well. Its construction was started by   Rana Veer Singh of the Vaghela dynasty of   Dandai Desh. But he was killed in a war, wherefater the Muslim king Mohammed   Begda of a neighbouring state built it in Indo-Islamic architectural style, in   1499.</p>
    <p>The cultural and architectural depiction in the deep wells at various levels   are a tribute to the history of step wells, built initially by Hindus and   subsequently ornamented and blended with Islamic architecture during the Muslim rule.<a href="#cite_note-detail-2"></a></p>
    <h2><span id="Legend">Legend</span></h2>
    <p>There is an interesting legend related to the building of this stepwell. In   the 15th century, Rana Veer Singh of the Vaghela dynasty, a Hindu ruler, reigned over this territory, then known as Dandai Desh.   His kingdom was attacked by Mohammed Begda, the Muslim ruler of a neighboring   kingdom. The battle resulted in the Rana king getting killed. Consequently his   territory was occupied by the invader. Rana Veer Singh&rsquo;s widow, a beautiful lady   known by the name Rani Roopba, though in deep grief at the death of her husband,   agreed to a marriage proposal made by Mohammed Begda on the condition that he   would first complete the building of the stepwell. The Muslim king who was   deeply enamoured of the queen&rsquo;s beauty agreed to the proposal and then built the   well in quick time, and with great interest. Once the well was completed, Begda   reminded the queen of her promise to marry him. But the queen who had achieved   her ambition of completing the stepwell started by her husband, decided to end   her life, as mark of devotion to her husband. She circumambulated the stepwell with prayers to   God and jumped into the well, thus ending the saga of building the well in   tragedy.</p>
    <p>The chronology of events that resulted in building of the stepwell is also   depicted on the walls of the well. There was no remorseful reaction by Begda as   he allowed the well to remain without any defacing.<a href="#cite_note-culture-3"></a></p>
    <p>Another legend narrated is linked to the tombs found near the well. The tombs   of six masons who built the well are seen near the <em>Vav</em>. It is said that   the Begda asked the Masons if they could build another similar well. But a   positive answer from them resulted in a further tragedy as Begda put them to   death. Begda was so enamoured of the architectural excellence of the stepwell   that he did not want its replica to be built.<a href="#cite_note-culture-3"></a></p>
    <h2><span id="Structure">Structure</span></h2>
