<p>The&nbsp;<strong>Sidi Bashir Mosque</strong>&nbsp;is located in the city of&nbsp;Ahmedabad,&nbsp;Gujarat. Due to its unique construction, the mosque is also called&nbsp;<strong>Jhulta Minar</strong>&nbsp;or<strong>Shaking Minarets</strong>.</p>
