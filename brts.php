  <p><strong>Ahmedabad BRTS</strong>&nbsp;is a highly ambitious rapid transport system developed by&nbsp;Gujarat Infrastructure Development Board (GIDB)&nbsp;for the city of&nbsp;Ahmedabad, recognizing that no single mode would cater to the mobility needs of the city and that &lsquo;Bus&rsquo; forms the most critical segment of the public transport system in the Ahmedabad city. GIDB has thereby entrusted the system design task to&nbsp;CEPT University. A part of first corridor connecting Pirana to R.T.O. was opened to public on October 14, 2009 by Chief Minister of Gujarat&nbsp;Narendra Modi.</p>
