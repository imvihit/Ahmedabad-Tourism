<p><strong>Sabarmati Ashram</strong> (Gujarati: &#2744;&#2750;&#2732;&#2736;&#2734;&#2724;&#2752;   &#2694;&#2742;&#2765;&#2736;&#2734; also known as Gandhi Ashram, Harijan Ashram,Satyagraha Ashram) is located in the   Ahmedabad suburb of Sabarmati adjoining to famous Ashram Road, at the bank of River Sabarmati, 4   miles from the town hall. This was one of the residences of Mohandas Karamchand Gandhi. This   ashram is now a national monument by the Government of India due to its   significance in the Indian independence movement in the form of the Dandi March in 1930.</p>
