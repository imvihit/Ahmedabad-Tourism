            <br />
            The shallow water reservoir speckled with reed beds gives it a distinct ambience while being an ideal dwelling for varied species of birds and insects. The agricultural land surrounding the lake provide ample amount of food for quite a number of birds while the hydrological ecosystem takes care of the others. One gets overwhelmed with flocks of bird flying together and crating graphical patterns on the sky while long-legged elongated necked cranes keep stalking for fishes and small insects. The still and serene ambience of the locale gets melodiously symphonized with the echoing of thousands birds and illustrated by the varied movements of these spirited creatures.   <br />
              
            <br />
            A visual treat for any tourist and a treasurable experience for a bird watcher or ornithologists, this place is home to more than 100 species of birds.  Cranes, geese, flamingoes, pelicans, egrets, herons, spoonbills, ducks, whistling teals and many other migratory birds nest and breed in the lap of this natures exuberance. The most commonly noticed Indian Saras Crane with its long bare legs, elongated head and red head gracefully pecks insects while walking around the lake with dance like movements. This bird which flaunts the status of being the tallest flying bird on earth is often see in pairs and has over centuries inspired poets and artists with its elegance and beauty. <br />
