    <li>In October 1999,&nbsp;<a href="http://en.wikipedia.org/wiki/Sachin_Tendulkar">Sachin Tendulkar</a>&nbsp;scored his first Test Match double-hundred in a match against New Zealand.</li>
    <li>Sachin Tendulkar completed his 20 years of International Cricket here on November 16th, 2009 against Sri Lanka. During the same game, Sachin Tendulkar reached a landmark of 30,000 runs in International cricket which by far no cricketer has achieved.</li>
    <li>Sachin Tendulkar became the first-ever cricketer to score 18,000 runs in One day cricket in a match against&nbsp;<a href="http://en.wikipedia.org/wiki/Australia_cricket_team" title="Australia cricket team">Australia</a>&nbsp;during 2011 Cricket World Cup.</li>
    <li><a href="http://en.wikipedia.org/wiki/AB_de_Villiers">AB de Villiers</a>&nbsp;reached his first double-hundred against India during the second test of South Africa's tour to India in 2008.</li>
  </ul>
