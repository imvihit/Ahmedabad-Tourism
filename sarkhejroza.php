  <p><strong>Sarkhej Roza</strong>&nbsp;is a mosque located in the village of&nbsp;Makraba, 7&nbsp;km south-west of&nbsp;Ahmedabad&nbsp;in&nbsp;Gujarat&nbsp;state, India. The mosque is known as &quot;Ahmedabad's Acropolis&quot;,<a href="http://en.wikipedia.org/wiki/Sarkhej_Roza#cite_note-0"></a>due to 20th century architect&nbsp;Le Corbusier's famous comparison of ths mosque's design to the&nbsp;Acropolis of Athens.</p>
  <p>Although there are many&nbsp;<em>rozas</em><a href="http://en.wikipedia.org/wiki/Sarkhej_Roza#cite_note-1"></a>&nbsp;across Gujarat, the Sarkhej Roza is the most revered. Sarkhej was once a prominent centre of&nbsp;Sufi&nbsp;culture in the country, where influential Sufi saint&nbsp;Ganj Baksh&nbsp;lived. It was on the saint's suggestion that Sultan&nbsp;Ahmed Shah&nbsp;set up his capital on the banks of the&nbsp;Sabarmati, a few miles away from&nbsp;Sarkhej.</p>
  <p>The Sarkhej Roza complex is located in the village of Makraba about 7&nbsp;km south-west of&nbsp;Ahmedabad.</p>
  <h2><span id="Overview_of_all_the_Monuments">Overview of all the Monuments</span></h2>
  <p>The Srakhej Roza complex has been interpreted as being composed of both 'jism'(body) and 'ruh'(spirit), giving it the qualities of a human being. The intricate stone carvings and stark beauty of the complex reflect the beauty of the soul of the roza's patron-saint Ganj Baksh. The complex was originally spread over 72 acres, surrounded by elaborate gardens on all sidea. Over time, human settlements came around it, eating into gardens and reducing the area to 34 acres.</p>
  <p>The arrangement of the tombs, palaces and the mosques around the large tank gives a visitor the sense of being in the presence of a formless, timeless entity. Like many monuments built during that period, the Sarkhej Roza fused bothMuslim&nbsp;and&nbsp;Hindu&nbsp;principles of architecture. While the ringed domes, the profusion of&nbsp;pillars&nbsp;and&nbsp;brackets&nbsp;follow the islamic genre, much of the ornamentation and&nbsp;motifs&nbsp;have Hindu designs. Most of the buildings don't have arches and depend on pierced stone&nbsp;trellises&nbsp;for stability. In its&nbsp;architecture, Sarkhej Roza is an example of the early&nbsp;Islamicarchitectural culture of the region, which fused Islamic stylistic influences from&nbsp;Persia&nbsp;with indigenous&nbsp;Hindu&nbsp;and&nbsp;Jainfeatures to form a composite &ldquo;Indo-Saracenic&rdquo;&nbsp;architectural style.<a href="http://en.wikipedia.org/wiki/Sarkhej_Roza#cite_note-2"></a>&nbsp;Although Sultan Qutubuddin Ahmed Shah II completed the roza between 1451 and 1458, it was the next sultan,&nbsp;Mehmud Begada, who gave the complex its present grandeur. He expanded it by building the pleasure palaces, gave finishing touches to the tank and added his own tomb just across the&nbsp;courtyard&nbsp;from the saint's. Sarkhej Roza one of the most elegant architectural complexes in the city, Sarkhej Roza is a unique complex of buildings.<a href="http://en.wikipedia.org/wiki/Sarkhej_Roza#cite_note-3"></a>&nbsp;The&nbsp;mosque, with its courtyard, creates a religious milieu; the royal connection is made through the&nbsp;tombs&nbsp;and&nbsp;palaces; the great tank, platforms and&nbsp;pavilions&nbsp;were used by the common man.</p>
